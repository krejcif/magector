// ─── Persistent Rust Serve Process ──────────────────────────────
// Keeps ONNX model + HNSW index loaded; eliminates ~2.6s cold start per query.
// Falls back to execFileSync if serve mode unavailable.

let serveProcess = null;
let serveReady = false;
let servePending = new Map();
let serveNextId = 1;
let serveReadline = null;
let serveReadyPromise = null;
let serveReadyResolve = null;

function startServeProcess() {
  // Guard: if a live serve process already exists (e.g. started by another
  // instance that won the lock race), reuse it instead of spawning a duplicate.
  const existingPid = getExistingServePid();
  if (existingPid) {
    logToFile('INFO', `Reusing existing serve process (PID ${existingPid}) — skipping spawn`);
    return;
  }

  serveReadyPromise = new Promise((resolve) => { serveReadyResolve = resolve; });
  try {
    const args = [
      'serve',
      '-d', config.dbPath,
      '-c', config.modelCache
    ];
    // Enable file watcher if magento root is configured
    if (config.magentoRoot && existsSync(config.magentoRoot)) {
      args.push('-m', config.magentoRoot, '--watch-interval', String(config.watchInterval));
    }
    // Pass descriptions DB path if it exists
    const descDbPath = path.join(config.magentoRoot || '.', '.magector', 'sqlite.db');
    if (existsSync(descDbPath)) {
      args.push('--descriptions-db', descDbPath);
    }
    // Pass thread limit if configured
    const threads = process.env.MAGECTOR_THREADS;
    if (threads) {
      args.push('--threads', threads);
    }
    logToFile('INFO', `Starting serve process: ${config.rustBinary} ${args.join(' ')}`);
    const proc = spawn(config.rustBinary, args,
      { stdio: ['pipe', 'pipe', 'pipe'], env: rustEnv });

    proc.on('error', (err) => {
      logToFile('ERR', `Serve process error: ${err.message}`);
      serveProcess = null; serveReady = false; removePidFile();
      if (serveReadyResolve) { serveReadyResolve(false); serveReadyResolve = null; }
    });
    proc.on('exit', (code, signal) => {
      logToFile('WARN', `Serve process exited (code=${code}, signal=${signal})`);
      serveProcess = null; serveReady = false; removePidFile();
      if (serveReadyResolve) { serveReadyResolve(false); serveReadyResolve = null; }
    });
    proc.stderr.on('data', (d) => {
      // Log serve process stderr (watcher events, tracing, errors) to .magector/magector.log
      // Strip ANSI escape codes for clean log output
      const text = d.toString().replace(/\x1b\[[0-9;]*m/g, '').trim();
      if (text) logToFile('SERVE', text);
    });

    serveReadline = createInterface({ input: proc.stdout });
    serveReadline.on('line', (line) => {
      let parsed;
      try { parsed = JSON.parse(line); } catch {
        logToFile('WARN', `Unparseable serve stdout: ${line.slice(0, 200)}`);
        return;
      }

      // First line is ready signal
      if (parsed.ready) {
        serveReady = true;
        logToFile('INFO', `Serve process ready (PID ${proc.pid})`);
        if (serveReadyResolve) { serveReadyResolve(true); serveReadyResolve = null; }
        return;
      }

      // Route response to pending request by order (FIFO)
      if (servePending.size > 0) {
        const [id, resolver] = servePending.entries().next().value;
        servePending.delete(id);
        resolver.resolve(parsed);
      }
    });

    serveProcess = proc;
    writePidFile(proc.pid);
    logToFile('INFO', `Serve process spawned (PID ${proc.pid})`);
  } catch (err) {
    logToFile('ERR', `Failed to start serve process: ${err.message}`);
    serveProcess = null;
    serveReady = false;
    if (serveReadyResolve) { serveReadyResolve(false); serveReadyResolve = null; }
  }
}

