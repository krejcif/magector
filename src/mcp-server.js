/**
 * Extract JSON from stdout that may contain tracing/log lines.
 * The npm-distributed binary can emit ANSI-colored tracing lines to stdout
 * even with RUST_LOG=error. This strips non-JSON lines before parsing.
 */
function extractJson(stdout) {
  const lines = stdout.split('\n');
  // Try each line from the end (JSON output is typically last)
  for (let i = lines.length - 1; i >= 0; i--) {
    const line = lines[i].trim();
    if (!line) continue;
    try {
      return JSON.parse(line);
    } catch {
      // not JSON, skip
    }
  }
  // Fallback: try parsing the entire output (handles multi-line JSON)
  // Strip lines that look like tracing (start with ANSI escape or timestamp bracket)
  // Also skip non-JSON prefix lines (e.g. "setting number of points 50000" from HNSW)
  const logLineRe = /^\s*(\x1b\[|\[[\d\-T:.Z]+)/;
  const jsonStartRe = /^\s*[\[{"\-0-9tfn]/;
  let startIdx = lines.findIndex(l => l.trim() && !logLineRe.test(l) && jsonStartRe.test(l));
  if (startIdx < 0) startIdx = 0;
  const cleaned = lines
    .slice(startIdx)
    .filter(l => !logLineRe.test(l) && l.trim())
    .join('\n')
    .trim();
  if (cleaned) {
    return JSON.parse(cleaned);
  }
  throw new SyntaxError('No valid JSON found in command output');
}

// ─── PID File & Orphan Cleanup ──────────────────────────────────
// Track the serve process PID to clean up orphans on restart.

const PID_PATH = path.join(config.magentoRoot, '.magector', 'serve.pid');
const REINDEX_PID_PATH = path.join(config.magentoRoot, '.magector', 'reindex.pid');
const SOCK_PATH = path.join(config.magentoRoot, '.magector', 'serve.sock');
const FORMAT_CACHE_PATH = path.join(config.magentoRoot, '.magector', 'format-ok.json');
const PRIMARY_LOCK_PATH = path.join(config.magentoRoot, '.magector', 'primary.lock');

/**
 * Try to acquire the primary lock (O_EXCL = atomic create-or-fail).
 * Returns true if we are the primary instance, false if another instance holds the lock.
 */
function tryAcquirePrimaryLock() {
  try {
    const fd = openSync(PRIMARY_LOCK_PATH, fsConstants.O_WRONLY | fsConstants.O_CREAT | fsConstants.O_EXCL);
    writeFileSync(fd, String(process.pid));
    closeSync(fd);
    return true;
  } catch {
    // Lock file exists — check if holder is alive
    try {
      const pid = parseInt(readFileSync(PRIMARY_LOCK_PATH, 'utf-8').trim(), 10);
      if (pid && !isNaN(pid)) {
        process.kill(pid, 0); // throws if dead
        return false; // another instance is alive and primary
      }
    } catch { /* holder is dead, take over */ }
    // Stale lock — reclaim. Use random jitter to avoid thundering herd
    // when multiple instances detect stale lock simultaneously.
    const jitterMs = Math.floor(Math.random() * 200) + 50;
    const start = Date.now();
    while (Date.now() - start < jitterMs) { /* busy-wait for sub-second jitter */ }
    try { unlinkSync(PRIMARY_LOCK_PATH); } catch {}
    try {
      const fd = openSync(PRIMARY_LOCK_PATH, fsConstants.O_WRONLY | fsConstants.O_CREAT | fsConstants.O_EXCL);
      writeFileSync(fd, String(process.pid));
      closeSync(fd);
      // Double-check: re-read to confirm we actually own it (no TOCTOU race)
      const check = readFileSync(PRIMARY_LOCK_PATH, 'utf-8').trim();
      if (check !== String(process.pid)) return false;
      return true;
    } catch {
      return false; // another instance beat us
    }
  }
}

function releasePrimaryLock() {
  try {
    // Only remove if we own it
    const content = readFileSync(PRIMARY_LOCK_PATH, 'utf-8').trim();
    if (content === String(process.pid)) {
      unlinkSync(PRIMARY_LOCK_PATH);
    }
  } catch {}
}

/**
 * Write the serve process PID to disk so future instances can clean up orphans.
 */
function writePidFile(pid) {
  try { writeFileSync(PID_PATH, `${pid}\n${__pkg.version}`); } catch {}
}

function getServePidVersion() {
  try {
    if (!existsSync(PID_PATH)) return null;
    const content = readFileSync(PID_PATH, 'utf-8').trim();
    const lines = content.split('\n');
    return lines[1] || null;
  } catch { return null; }
}

function removePidFile() {
  try { if (existsSync(PID_PATH)) unlinkSync(PID_PATH); } catch {}
}

function writeReindexPidFile(pid) {
  try { writeFileSync(REINDEX_PID_PATH, String(pid)); } catch {}
}

function removeReindexPidFile() {
  try { if (existsSync(REINDEX_PID_PATH)) unlinkSync(REINDEX_PID_PATH); } catch {}
}

/**
 * Check if another reindex process is already running (from another MCP instance).
 * Returns the PID if alive, null otherwise.
 */
function getRunningReindexPid() {
  try {
    if (!existsSync(REINDEX_PID_PATH)) return null;
    const pid = parseInt(readFileSync(REINDEX_PID_PATH, 'utf-8').trim(), 10);
    if (!pid || isNaN(pid)) return null;
    process.kill(pid, 0); // signal 0 = existence check
    return pid;
  } catch {
    // Process doesn't exist or PID file unreadable — clean up
    removeReindexPidFile();
    return null;
  }
}

/**
 * Check if an existing serve process is alive and usable.
 * Returns the PID if alive, null if stale/missing.
 * Does NOT kill it — multiple MCP instances can share one serve process
 * by sending queries to it via stdin (each instance starts its own).
 */
function getExistingServePid() {
  try {
    if (!existsSync(PID_PATH)) return null;
    const pid = parseInt(readFileSync(PID_PATH, 'utf-8').trim(), 10);
    if (!pid || isNaN(pid)) return null;
    process.kill(pid, 0); // signal 0 = existence check
    return pid;
  } catch {
    removePidFile();
    return null;
  }
}

/**
 * Kill any stale serve process from a previous MCP server instance.
 * Only called during cleanup (exit/SIGTERM), not during startup —
 * multiple concurrent MCP instances each run their own serve process.
 */
function killStaleServeProcess() {
  try {
    if (!existsSync(PID_PATH)) return;
    const stalePid = parseInt(readFileSync(PID_PATH, 'utf-8').trim(), 10);
    if (!stalePid || isNaN(stalePid)) return;

    try {
      process.kill(stalePid, 0);
    } catch {
      removePidFile();
      return;
    }

    logToFile('WARN', `Killing stale serve process (PID ${stalePid}) from previous session`);
    console.error(`Killing stale serve process (PID ${stalePid})`);
    try { process.kill(stalePid, 'SIGTERM'); } catch {}

    setTimeout(() => {
      try {
        process.kill(stalePid, 0);
        process.kill(stalePid, 'SIGKILL');
      } catch {}
    }, 2000);

    removePidFile();
  } catch {
    // Non-fatal
  }
}

// ─── Database Format Check & Background Re-index ────────────────

let reindexInProgress = false;
let reindexProcess = null;
let warmupInProgress = true; // true until checkDbFormat + serve process ready

/**
 * Check if the database file is compatible with the current binary.
 * Uses a cached result file to avoid running stats (30-60s) on every startup.
 * Cache key: binary path mtime + db file mtime + db size.
 */
async function checkDbFormat() {
  if (!existsSync(config.dbPath)) return true;

  try {
    const fstat = statSync(config.dbPath);
    if (fstat.size < 100) return true;

    // Check cached result — avoids 40s stats command on every MCP startup
    const binaryStat = statSync(config.rustBinary);
    const cacheKey = `${binaryStat.mtimeMs}|${fstat.mtimeMs}|${fstat.size}`;
    try {
      const cached = JSON.parse(readFileSync(FORMAT_CACHE_PATH, 'utf-8'));
      if (cached.key === cacheKey) {
        logToFile('INFO', `Format check cached: ${cached.ok ? 'compatible' : 'incompatible'}`);
        return cached.ok;
      }
    } catch { /* no cache or invalid */ }

    // Cache miss — run stats (expensive: loads full HNSW graph)
    logToFile('INFO', 'Format check: running stats (this takes 30-60s for large indexes)...');
    const result = await new Promise((resolve, reject) => {
      const proc = spawn(config.rustBinary, ['stats', '-d', config.dbPath],
        { stdio: ['pipe', 'pipe', 'pipe'], env: rustEnv });
      let stdout = '';
      proc.stdout.on('data', (d) => { stdout += d.toString(); });
      proc.on('error', reject);
      proc.on('exit', (code) => code === 0 ? resolve(stdout) : reject(new Error(`stats exit ${code}`)));
      setTimeout(() => { try { proc.kill(); } catch {} reject(new Error('stats timeout')); }, 120000);
    });

    const vectors = parseInt(result.match(/Total vectors:\s*(\d+)/)?.[1] || '0');
    const ok = vectors > 0;

    // Write cache
    try { writeFileSync(FORMAT_CACHE_PATH, JSON.stringify({ key: cacheKey, ok })); } catch {}
    return ok;
  } catch {
    return false;
  }
}

/**
 * Start a background re-index process that builds to a temporary path.
 * The old DB remains in place so search tools keep working during rebuild.
 * On completion, the new index is swapped in atomically (old → .bak, new → current).
 */
function startBackgroundReindex() {
  if (reindexInProgress) return;

  // Check if another MCP instance is already running a reindex
  const existingPid = getRunningReindexPid();
  if (existingPid) {
    logToFile('INFO', `Reindex already running in another process (PID ${existingPid}) — skipping`);
    console.error(`Reindex already running (PID ${existingPid}) — skipping`);
    reindexInProgress = true; // mark locally so tools know
    // Poll the external process and react when it finishes
    const tempDbPath = config.dbPath + '.new';
    const pollInterval = setInterval(() => {
      if (!getRunningReindexPid()) {
        clearInterval(pollInterval);
        reindexInProgress = false;
        // Swap the new DB into place if the external reindex produced one
        if (existsSync(tempDbPath)) {
          try {
            if (existsSync(config.dbPath)) {
              const backupPath = config.dbPath + '.bak';
              if (existsSync(backupPath)) { try { unlinkSync(backupPath); } catch {} }
              renameSync(config.dbPath, backupPath);
              logToFile('INFO', 'Old DB moved to .bak');
            }
            renameSync(tempDbPath, config.dbPath);
            logToFile('INFO', 'External reindex complete — new index swapped into place.');
          } catch (e) {
            logToFile('ERR', `Failed to swap index after external reindex: ${e.message}`);
          }
        } else {
          logToFile('INFO', 'External reindex finished but no .new file found — skipping swap.');
        }
        if (serveProcess) serveProcess.kill();
        searchCache.clear();
        startServeProcess();
      }
    }, 10000);
    return;
  }

  if (!config.magentoRoot || !existsSync(config.magentoRoot)) {
    const msg = 'Cannot auto-reindex: MAGENTO_ROOT not set or not found';
    console.error(msg);
    logToFile('WARN', msg);
    return;
  }

  const tempDbPath = config.dbPath + '.new';

  // Clean up leftover temp DB from a previous failed reindex
  if (existsSync(tempDbPath)) {
    try { unlinkSync(tempDbPath); } catch {}
  }

  reindexInProgress = true;

  const hadExistingDb = existsSync(config.dbPath);
  logToFile('WARN', `Starting background re-index to temp path. Old DB ${hadExistingDb ? 'preserved for queries' : 'not found'}.`);
  console.error(`Database format incompatible. Starting background re-index (log: ${LOG_PATH})`);

  const reindexArgs = [
    'index',
    '-m', config.magentoRoot,
    '-d', tempDbPath,
    '-c', config.modelCache
  ];
  const threads = process.env.MAGECTOR_THREADS;
  if (threads) {
    reindexArgs.push('--threads', threads);
  }
  const bgDescDbPath = path.join(config.magentoRoot, '.magector', 'sqlite.db');
  if (existsSync(bgDescDbPath)) {
    reindexArgs.push('--descriptions-db', bgDescDbPath);
  }
  logToFile('INFO', `Starting background reindex: ${config.rustBinary} ${reindexArgs.join(' ')}`);
  reindexProcess = spawn(config.rustBinary, reindexArgs, {
    stdio: ['pipe', 'pipe', 'pipe'],
    env: rustEnv,
  });

  // Write PID file so other MCP instances know a reindex is running
  writeReindexPidFile(reindexProcess.pid);

  // Log stdout/stderr line-by-line using readline to avoid buffering issues.
  // Without this, Rust tracing output accumulates in pipe buffers and progress
  // entries arrive in large chunks instead of in real time.
  const indexStdout = createInterface({ input: reindexProcess.stdout });
  const indexStderr = createInterface({ input: reindexProcess.stderr });
  indexStdout.on('line', (line) => {
    const text = line.replace(/\x1b\[[0-9;]*m/g, '').trim();
    if (text) logToFile('INDEX', text);
  });
  indexStderr.on('line', (line) => {
    const text = line.replace(/\x1b\[[0-9;]*m/g, '').trim();
    if (text) logToFile('INDEX', text);
  });

  reindexProcess.on('exit', (code) => {
    reindexInProgress = false;
    reindexProcess = null;
    removeReindexPidFile();
    if (code === 0) {
      // Atomic swap: old → .bak, new → current
      try {
        if (existsSync(config.dbPath)) {
          const backupPath = config.dbPath + '.bak';
          if (existsSync(backupPath)) { try { unlinkSync(backupPath); } catch {} }
          renameSync(config.dbPath, backupPath);
          logToFile('INFO', 'Old DB moved to .bak');
        }
        renameSync(tempDbPath, config.dbPath);
        logToFile('INFO', 'New index swapped into place.');
      } catch (e) {
        logToFile('ERR', `Failed to swap index: ${e.message}`);
      }
      logToFile('INFO', 'Background re-index completed. Restarting serve process.');
      console.error('Background re-index completed. Restarting serve process.');
      if (serveProcess) serveProcess.kill();
      searchCache.clear();
      startServeProcess();
    } else {
      // Clean up failed temp DB
      try { if (existsSync(tempDbPath)) unlinkSync(tempDbPath); } catch {}
      logToFile('ERR', `Background re-index failed (exit code ${code})`);
      console.error(`Background re-index failed (exit code ${code}). Check ${LOG_PATH}`);
    }
  });

  reindexProcess.on('error', (err) => {
    reindexInProgress = false;
    reindexProcess = null;
    removeReindexPidFile();
    logToFile('ERR', `Background re-index error: ${err.message}`);
    console.error(`Background re-index error: ${err.message}`);
  });
}

/**
 * Query cache: avoid re-embedding identical queries.
 * Keyed by "query|limit", capped at 200 entries (LRU eviction).
 */
const searchCache = new Map();
const CACHE_MAX = 200;

function cacheSet(key, value) {
  if (searchCache.size >= CACHE_MAX) {
    const oldest = searchCache.keys().next().value;
    searchCache.delete(oldest);
  }
  searchCache.set(key, value);
}

// ─── SONA: MCP Feedback Signal Tracker ────────────────────────
class SessionTracker {
  constructor() {
    this.lastSearch = null;       // {query, resultPaths, timestamp}
    this.feedbackQueue = [];
  }

  recordToolCall(toolName, args, results) {
    const now = Date.now();

    if (toolName === 'magento_search') {
      // If previous search was < 60s ago and query differs → query_refinement
      if (this.lastSearch && (now - this.lastSearch.timestamp) < 60000
          && args.query !== this.lastSearch.query) {
        this.feedbackQueue.push({
          type: 'query_refinement',
          originalQuery: this.lastSearch.query,
          refinedQuery: args.query,
          originalResultPaths: this.lastSearch.resultPaths,
          timestamp: now
        });
      }
      this.lastSearch = {
        query: args.query,
        resultPaths: (results || []).map(r => r.path || r.metadata?.path).filter(Boolean),
        timestamp: now
      };
      return;
    }

    // Follow-up tool after search (within 30s)
    if (this.lastSearch && (now - this.lastSearch.timestamp) < 30000) {
      const refinementMap = {
        'magento_find_plugin': 'refinement_to_plugin',
        'magento_find_class': 'refinement_to_class',
        'magento_find_config': 'refinement_to_config',
        'magento_find_observer': 'refinement_to_observer',
        'magento_find_controller': 'refinement_to_controller',
        'magento_find_block': 'refinement_to_block',
        'magento_trace_flow': 'trace_after_search',
      };
      const signalType = refinementMap[toolName];
      if (signalType) {
        this.feedbackQueue.push({
          type: signalType,
          query: this.lastSearch.query,
          searchResultPaths: this.lastSearch.resultPaths,
          followedTool: toolName,
          followedArgs: args,
          timestamp: now
        });
      }
    }
  }

  flush() {
    const signals = this.feedbackQueue;
    this.feedbackQueue = [];
    return signals;
  }
}

